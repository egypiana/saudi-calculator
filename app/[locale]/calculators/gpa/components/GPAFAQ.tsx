export default function GPAFAQ() {
  const [openIndex, setOpenIndex] = useState<number | null>(0);

  const faqJsonLd = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: FAQS.map((faq) => ({
      "@type": "Question",
      name: faq.q,
      acceptedAnswer: {
        "@type": "Answer",
        text: faq.a,
      },
    })),
  };

  return (
    <div className="bg-white dark:bg-dark-surface rounded-2xl border border-gray-200 dark:border-gray-700 overflow-hidden">
      <script type="application/ld+json" dangerouslySetInnerHTML={{ __html: JSON.stringify(faqJsonLd) }} />

      <div className="px-5 py-4 border-b border-gray-100 dark:border-gray-800 bg-gray-50 dark:bg-dark-bg">
        <h2 className="font-bold text-gray-800 dark:text-white text-sm flex items-center gap-2">
          ❓ أسئلة شائعة عن المعدل التراكمي
          <span className="text-xs font-normal text-gray-400">({FAQS.length} سؤال)</span>
        </h2>
      </div>

      <div className="divide-y divide-gray-100 dark:divide-gray-800">
        {FAQS.map((faq, i) => {
          const isOpen = openIndex === i;
          return (
            <div key={i}>
              <button
                onClick={() => setOpenIndex(isOpen ? null : i)}
                className="w-full flex items-center justify-between px-5 py-4 text-right hover:bg-gray-50 dark:hover:bg-dark-bg/50 transition-colors"
              >
                <span className="font-medium text-sm text-gray-800 dark:text-white leading-relaxed pl-4">{faq.q}</span>
                <ChevronDown className={`h-4 w-4 flex-shrink-0 text-gray-400 transition-transform ${isOpen ? "rotate-180" : ""}`} />
              </button>
              {isOpen && (
                <div className="px-5 pb-4">
                  <p className="text-sm text-gray-600 dark:text-gray-400 leading-relaxed bg-purple-50 dark:bg-purple-900/10 rounded-xl px-4 py-3">
                    {faq.a}
                  </p>
                </div>
              )}
            </div>
          );
        })}
      </div>
    </div>
  );
}
