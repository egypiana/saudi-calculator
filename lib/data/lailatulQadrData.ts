  night23Date: string;
  night25Date: string;
  night29Date: string;
  night21DayOfWeek: string;
  night23DayOfWeek: string;
  night25DayOfWeek: string;
  night27DayOfWeek: string;
  night29DayOfWeek: string;
  lastTenStart: string;
  season: "شتاء" | "ربيع" | "صيف" | "خريف";
  status: "past" | "current" | "upcoming" | "future";
  content: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
    heroSubtitle: string;
    mainContent: string;
    worshipTip: string;
    uniqueFact: string;
    faqExtra: { q: string; a: string }[];
  };
