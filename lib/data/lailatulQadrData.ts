const DAYS_AR: Record<number, string> = {
  0: "الأحد", 1: "الاثنين", 2: "الثلاثاء", 3: "الأربعاء",
  4: "الخميس", 5: "الجمعة", 6: "السبت",
};

function getDayOfWeek(dateStr: string): string {
  const d = new Date(dateStr + "T00:00:00");
  return DAYS_AR[d.getDay()];
}

function addDays(dateStr: string, days: number): string {
  const d = new Date(dateStr + "T00:00:00");
  d.setDate(d.getDate() + days);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}-${m}-${day}`;
}

function getSeason(night27Date: string): LailatulQadrYearData["season"] {
  const month = new Date(night27Date + "T00:00:00").getMonth() + 1;
  if (month === 12 || month === 1 || month === 2) return "شتاء";
  if (month >= 3 && month <= 5) return "ربيع";
  if (month >= 6 && month <= 8) return "صيف";
  return "خريف";
}

function getStatus(ramadanStart: string): LailatulQadrYearData["status"] {
  const now = new Date();
  const night21 = new Date(addDays(ramadanStart, 20) + "T00:00:00");
  const night29End = new Date(addDays(ramadanStart, 29) + "T00:00:00");
  if (now > night29End) return "past";
  if (now >= night21 && now <= night29End) return "current";
  const diff = night21.getTime() - now.getTime();
  if (diff < 365 * 24 * 60 * 60 * 1000) return "upcoming";
  return "future";
}

export function formatDateAr(dateStr: string): string {
  const d = new Date(dateStr + "T00:00:00");
  const months = [
    "يناير", "فبراير", "مارس", "أبريل", "مايو", "يونيو",
    "يوليو", "أغسطس", "سبتمبر", "أكتوبر", "نوفمبر", "ديسمبر",
  ];
  return `${d.getDate()} ${months[d.getMonth()]} ${d.getFullYear()}`;
}

// Alias for backward compatibility
export const formatArabicDate = formatDateAr;

function makeYear(
  year: number,
  hijriYear: number,
  ramadanStart: string,
  heroSubtitle: string,
  mainContent: string,
  worshipTip: string,
  uniqueFact: string,
  faqExtra: { q: string; a: string }[]
): LailatulQadrYearData {
  const night21 = addDays(ramadanStart, 20);
  const night23 = addDays(ramadanStart, 22);
  const night25 = addDays(ramadanStart, 24);
  const night27 = addDays(ramadanStart, 26);
  const night29 = addDays(ramadanStart, 28);
  const season = getSeason(night27);

  return {
    year,
    hijriYear,
    hijri: `${hijriYear} هـ`,
    ramadanStart,
    night27Date: night27,
    oddNights: { night21, night23, night25, night27, night29 },
    night21Date: night21,
    night23Date: night23,
    night25Date: night25,
    night29Date: night29,
    night21DayOfWeek: getDayOfWeek(night21),
    night23DayOfWeek: getDayOfWeek(night23),
    night25DayOfWeek: getDayOfWeek(night25),
    night27DayOfWeek: getDayOfWeek(night27),
    night29DayOfWeek: getDayOfWeek(night29),
    lastTenStart: night21,
    season,
    status: getStatus(ramadanStart),
    content: {
      metaTitle: `ليلة القدر ${year} | 27 رمضان ${hijriYear} هـ`,
      metaDescription: `موعد ليلة القدر ${year} في ${formatDateAr(night27)}. تعرف على العشر الأواخر من رمضان ${hijriYear} هـ والليالي الوترية وأفضل الأدعية والأعمال.`,
      keywords: [
        `ليلة القدر ${year}`,
        `ليلة القدر ${hijriYear}`,
        `27 رمضان ${year}`,
        `العشر الأواخر ${year}`,
        `متى ليلة القدر ${year}`,
        `دعاء ليلة القدر`,
      ],
      heroSubtitle,
      mainContent,
      worshipTip,
      uniqueFact,
      faqExtra,
    },
  };
}

