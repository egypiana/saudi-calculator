export const LAILATUL_QADR_DATA: Record<number, LailatulQadrYearData> = data;

export const LAILATUL_QADR_YEARS: number[] = Object.keys(data)
  .map(Number)
  .sort((a, b) => a - b);

// Array format for components that need it
export const LAILATUL_QADR_LIST: LailatulQadrYearData[] = LAILATUL_QADR_YEARS.map((y) => data[y]);

export function getNextLailatulQadr(): LailatulQadrYearData | null {
  const now = new Date();
  for (const year of LAILATUL_QADR_YEARS) {
    const entry = data[year];
    const night29End = new Date(entry.oddNights.night29 + "T23:59:59");
    if (now <= night29End) return entry;
  }
  return null;
}

export function getTimeUntilLailatulQadr(year?: number): {
  days: number;
  hours: number;
  minutes: number;
  seconds: number;
  targetDate: string;
  isPast: boolean;
} {
  let targetEntry: LailatulQadrYearData | null = null;

  if (year !== undefined && data[year]) {
    targetEntry = data[year];
  } else {
    targetEntry = getNextLailatulQadr();
  }

  if (!targetEntry) return { days: 0, hours: 0, minutes: 0, seconds: 0, targetDate: "", isPast: true };

  const now = new Date();
  const target = new Date(targetEntry.night27Date + "T18:00:00");

  const diff = target.getTime() - now.getTime();
  const isPast = diff < 0;
  const absDiff = Math.abs(diff);

  const days = Math.floor(absDiff / (1000 * 60 * 60 * 24));
  const hours = Math.floor((absDiff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  const minutes = Math.floor((absDiff % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((absDiff % (1000 * 60)) / 1000);

  return { days, hours, minutes, seconds, targetDate: targetEntry.night27Date, isPast };
}
