// Build data from Ramadan dates
function buildData(): Record<number, LailatulQadrYearData> {
  const result: Record<number, LailatulQadrYearData> = {};

  for (const r of RAMADAN_DATES) {
    const night21 = addDays(r.startDate, 20);
    const night23 = addDays(r.startDate, 22);
    const night25 = addDays(r.startDate, 24);
    const night27 = addDays(r.startDate, 26);
    const night29 = addDays(r.startDate, 28);
    const season = getSeason(night27);
    const hijriNum = parseInt(r.hijri.replace(/[^\d]/g, ""));

    const content = getYearContent(r.year, hijriNum, season, night27);

    result[r.year] = {
      year: r.year,
      hijriYear: hijriNum,
      hijri: r.hijri,
      ramadanStart: r.startDate,
      night21Date: night21,
      night23Date: night23,
      night25Date: night25,
      night27Date: night27,
      night29Date: night29,
      lastTenStart: night21,
      night21DayOfWeek: getDayOfWeek(night21),
      night23DayOfWeek: getDayOfWeek(night23),
      night25DayOfWeek: getDayOfWeek(night25),
      night27DayOfWeek: getDayOfWeek(night27),
      night29DayOfWeek: getDayOfWeek(night29),
      season,
      status: getStatus(night27, night29),
      content: {
        metaTitle: `ليلة القدر ${r.year} | ٢٧ رمضان ${hijriNum} هـ | العد التنازلي`,
        metaDescription: `كم باقي على ليلة القدر ${r.year}؟ ليلة 27 رمضان ${hijriNum} هـ توافق ${formatDateAr(night27)}. عداد تنازلي دقيق مع الليالي الوترية وأدعية ليلة القدر.`,
        keywords: [`ليلة القدر ${r.year}`, `ليلة القدر ${hijriNum}`, `متى ليلة القدر ${r.year}`, `ليلة 27 رمضان ${r.year}`, `العشر الأواخر ${r.year}`],
        ...content,
      },
    };
  }

  return result;
}

export const LAILATUL_QADR_DATA = buildData();
export const LAILATUL_QADR_YEARS = Object.keys(LAILATUL_QADR_DATA).map(Number).sort((a, b) => a - b);

// Array format for components that need it
export const LAILATUL_QADR_LIST = LAILATUL_QADR_YEARS.map((y) => LAILATUL_QADR_DATA[y]);

export function getNextLailatulQadr(): LailatulQadrYearData | null {
  const now = new Date();
  const todayStr = now.toISOString().slice(0, 10);
  for (const year of LAILATUL_QADR_YEARS) {
    const data = LAILATUL_QADR_DATA[year];
    if (data.night29Date >= todayStr) return data;
  }
  return null;
}

export function getTimeUntilLailatulQadr(year: number): { days: number; hours: number; minutes: number; seconds: number } {
  const data = LAILATUL_QADR_DATA[year];
  if (!data) return { days: 0, hours: 0, minutes: 0, seconds: 0 };

  const target = new Date(data.night27Date + "T00:00:00").getTime();
  const now = new Date().getTime();
  const diff = Math.max(0, target - now);

  return {
    days: Math.floor(diff / (1000 * 60 * 60 * 24)),
    hours: Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)),
    minutes: Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60)),
    seconds: Math.floor((diff % (1000 * 60)) / 1000),
  };
}
