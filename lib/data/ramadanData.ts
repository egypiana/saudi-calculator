export const RAMADAN_DATA: Record<number, RamadanYearData> = {};
DATA_LIST.forEach((d) => { RAMADAN_DATA[d.year] = d; });

export const AVAILABLE_YEARS = DATA_LIST.map((d) => d.year);

export function getTimeUntilRamadan(year: number): {
  days: number; hours: number; minutes: number; seconds: number; isPast: boolean;
} {
  const data = RAMADAN_DATA[year];
  if (!data) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  const target = new Date(data.startDate);
  const now = new Date();
  const diff = target.getTime() - now.getTime();
  if (diff <= 0) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  return {
    days: Math.floor(diff / (1000 * 60 * 60 * 24)),
    hours: Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60)),
    minutes: Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60)),
    seconds: Math.floor((diff % (1000 * 60)) / 1000),
    isPast: false,
  };
}
