export interface RamadanYearData {
  year: number;
  hijriYear: number;
  startDate: string;
  endDate: string;
  eidFitrDate: string;
  days: number;
  startDayOfWeek: string;
  season: "شتاء" | "ربيع" | "صيف" | "خريف";
  fastingHoursRiyadh: number;
  status: "past" | "current" | "upcoming" | "future";
  content: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
    heroSubtitle: string;
    mainContent: string;
    preparationTip: string;
    uniqueFact: string;
    eidMessage: string;
    faqExtra: { q: string; a: string }[];
  };
}

function getStatus(startDate: string, endDate: string): RamadanYearData["status"] {
  const now = new Date();
  const start = new Date(startDate);
  const end = new Date(endDate);
  if (now > end) return "past";
  if (now >= start && now <= end) return "current";
  const diff = start.getTime() - now.getTime();
  if (diff < 365 * 24 * 60 * 60 * 1000) return "upcoming";
  return "future";
}

function makeYear(
  year: number, hijriYear: number, startDate: string, endDate: string,
  eidFitrDate: string, days: number, dayOfWeek: string,
  season: RamadanYearData["season"], fastingHours: number,
  heroSubtitle: string, mainContent: string, preparationTip: string,
  uniqueFact: string, faqExtra: { q: string; a: string }[]
): RamadanYearData {
  return {
    year, hijriYear, startDate, endDate, eidFitrDate, days,
    startDayOfWeek: dayOfWeek, season, fastingHoursRiyadh: fastingHours,
    status: getStatus(startDate, endDate),
    content: {
      metaTitle: `العد التنازلي لرمضان ${year} | رمضان ${hijriYear} هـ`,
      metaDescription: `كم باقي على رمضان ${year}؟ يبدأ رمضان ${hijriYear} هـ في ${formatDateAr(startDate)}. عداد تنازلي دقيق مع تواريخ ومعلومات الصيام في السعودية.`,
      keywords: [`رمضان ${year}`, `رمضان ${hijriYear}`, `متى رمضان ${year}`, `كم باقي على رمضان ${year}`],
      heroSubtitle,
      mainContent,
      preparationTip,
      uniqueFact,
      eidMessage: `عيد الفطر ${year} — ${formatDateAr(eidFitrDate)} / 1 شوال ${hijriYear} هـ`,
      faqExtra,
    },
  };
}

function formatDateAr(dateStr: string): string {
  const months: Record<string, string> = {
    "01": "يناير", "02": "فبراير", "03": "مارس", "04": "أبريل",
    "05": "مايو", "06": "يونيو", "07": "يوليو", "08": "أغسطس",
    "09": "سبتمبر", "10": "أكتوبر", "11": "نوفمبر", "12": "ديسمبر",
  };
  const [y, m, d] = dateStr.split("-");
  return `${parseInt(d)} ${months[m]} ${y}`;
}

