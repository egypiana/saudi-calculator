export interface HajjYearData {
  year: number;
  hijriYear: number;
  hijriMonth: string;
  date: string;
  dayOfWeek: string;
  arafaDay: string;
  eidDay: string;
  endDate: string;
  season: "شتاء" | "ربيع" | "صيف" | "خريف";
  status: "past" | "current" | "upcoming" | "future";
  content: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
    heroSubtitle: string;
    uniqueFact: string;
    mainContent: string;
    preparations: string[];
    faqExtra: { q: string; a: string }[];
  };
}

function getStatus(date: string): HajjYearData["status"] {
  const now = new Date();
  const hajjStart = new Date(date);
  const hajjEnd = new Date(hajjStart);
  hajjEnd.setDate(hajjEnd.getDate() + 6);
  if (now > hajjEnd) return "past";
  if (now >= hajjStart && now <= hajjEnd) return "current";
  const diff = hajjStart.getTime() - now.getTime();
  if (diff < 365 * 24 * 60 * 60 * 1000) return "upcoming";
  return "future";
}

export function formatDateAr(dateStr: string): string {
  const months: Record<string, string> = {
    "01": "يناير", "02": "فبراير", "03": "مارس", "04": "أبريل",
    "05": "مايو", "06": "يونيو", "07": "يوليو", "08": "أغسطس",
    "09": "سبتمبر", "10": "أكتوبر", "11": "نوفمبر", "12": "ديسمبر",
  };
  const [y, m, d] = dateStr.split("-");
  return `${parseInt(d)} ${months[m]} ${y}`;
}

function getDayOfWeek(dateStr: string): string {
  const days = ["الأحد", "الاثنين", "الثلاثاء", "الأربعاء", "الخميس", "الجمعة", "السبت"];
  return days[new Date(dateStr + "T00:00:00").getDay()];
}

function addDays(dateStr: string, n: number): string {
  const d = new Date(dateStr + "T00:00:00");
  d.setDate(d.getDate() + n);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${y}-${m}-${day}`;
}

function makeHajj(
  year: number, hijriYear: number, date: string,
  arafaDay: string, eidDay: string,
  season: HajjYearData["season"],
  heroSubtitle: string, uniqueFact: string,
  mainContent: string, preparations: string[],
  faqExtra: { q: string; a: string }[]
): HajjYearData {
  const endDate = addDays(eidDay, 3);
  return {
    year, hijriYear, hijriMonth: "8-13 ذو الحجة", date,
    dayOfWeek: getDayOfWeek(date),
    arafaDay, eidDay, endDate, season,
    status: getStatus(date),
    content: {
      metaTitle: `موسم الحج ${year} | ${hijriYear} هـ — كم باقي على الحج؟`,
      metaDescription: `تابع العد التنازلي لموسم الحج ${year} الموافق ${formatDateAr(date)} / 8 ذو الحجة ${hijriYear} هـ. عداد تنازلي دقيق ومعلومات كاملة عن موسم الحج.`,
      keywords: [`الحج ${year}`, `حج ${hijriYear}`, `متى الحج ${year}`, `كم باقي على الحج ${year}`],
      heroSubtitle, uniqueFact, mainContent, preparations, faqExtra,
    },
  };
}

