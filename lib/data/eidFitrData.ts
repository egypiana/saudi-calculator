export const EID_FITR_DATA: Record<number, EidFitrYearData> = {};
DATA_LIST.forEach((d) => { EID_FITR_DATA[d.year] = d; });

export const EID_YEARS = DATA_LIST.map((d) => d.year);

export function getTimeUntilEid(year: number) {
  const data = EID_FITR_DATA[year];
  if (!data) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  const target = new Date(data.date + "T00:00:00");
  const now = new Date();
  const diff = target.getTime() - now.getTime();
  if (diff <= 0) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  return {
    days: Math.floor(diff / 86400000),
    hours: Math.floor((diff % 86400000) / 3600000),
    minutes: Math.floor((diff % 3600000) / 60000),
    seconds: Math.floor((diff % 60000) / 1000),
    isPast: false,
  };
}

export function getNextEidYear(): number {
  const now = new Date();
  for (const year of EID_YEARS) {
    const data = EID_FITR_DATA[year];
    if (data && new Date(data.date + "T00:00:00") > now) return year;
  }
  return EID_YEARS[EID_YEARS.length - 1];
}
