export interface EidFitrYearData {
  year: number;
  hijriYear: number;
  hijriMonth: string;
  date: string;
  dayOfWeek: string;
  ramadanStart: string;
  ramadanDays: number;
  season: "شتاء" | "ربيع" | "صيف" | "خريف";
  status: "past" | "current" | "upcoming" | "future";
  content: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
    heroSubtitle: string;
    uniqueFact: string;
    mainContent: string;
    preparations: string[];
    faqExtra: { q: string; a: string }[];
  };
}

function getStatus(date: string): EidFitrYearData["status"] {
  const now = new Date();
  const eid = new Date(date);
  const eidEnd = new Date(eid);
  eidEnd.setDate(eidEnd.getDate() + 3);
  if (now > eidEnd) return "past";
  if (now >= eid && now <= eidEnd) return "current";
  const diff = eid.getTime() - now.getTime();
  if (diff < 365 * 24 * 60 * 60 * 1000) return "upcoming";
  return "future";
}

export function formatDateAr(dateStr: string): string {
  const months: Record<string, string> = {
    "01": "يناير", "02": "فبراير", "03": "مارس", "04": "أبريل",
    "05": "مايو", "06": "يونيو", "07": "يوليو", "08": "أغسطس",
    "09": "سبتمبر", "10": "أكتوبر", "11": "نوفمبر", "12": "ديسمبر",
  };
  const [y, m, d] = dateStr.split("-");
  return `${parseInt(d)} ${months[m]} ${y}`;
}

function getDayOfWeek(dateStr: string): string {
  const days = ["الأحد", "الاثنين", "الثلاثاء", "الأربعاء", "الخميس", "الجمعة", "السبت"];
  return days[new Date(dateStr + "T00:00:00").getDay()];
}

function makeEid(
  year: number, hijriYear: number, date: string,
  ramadanStart: string, ramadanDays: number,
  season: EidFitrYearData["season"],
  heroSubtitle: string, uniqueFact: string,
  mainContent: string, preparations: string[],
  faqExtra: { q: string; a: string }[]
): EidFitrYearData {
  return {
    year, hijriYear, hijriMonth: "1 شوال", date,
    dayOfWeek: getDayOfWeek(date),
    ramadanStart, ramadanDays, season,
    status: getStatus(date),
    content: {
      metaTitle: `عيد الفطر ${year} | 1 شوال ${hijriYear} هـ — كم باقي على العيد؟`,
      metaDescription: `تابع العد التنازلي لعيد الفطر ${year} الموافق ${formatDateAr(date)} / 1 شوال ${hijriYear} هـ. عداد تنازلي دقيق ومعلومات كاملة عن العيد المبارك.`,
      keywords: [`عيد الفطر ${year}`, `عيد الفطر ${hijriYear}`, `متى عيد الفطر ${year}`, `كم باقي على عيد الفطر ${year}`],
      heroSubtitle, uniqueFact, mainContent, preparations, faqExtra,
    },
  };
}

