export const NATIONAL_DAY_DATA: Record<number, NationalDayYearData> = {};
DATA_LIST.forEach((d) => { NATIONAL_DAY_DATA[d.year] = d; });

export const NATIONAL_DAY_YEARS: number[] = DATA_LIST.map((d) => d.year);

export function getTimeUntilNationalDay(year: number) {
  const data = NATIONAL_DAY_DATA[year];
  if (!data) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  const target = new Date(data.date + "T00:00:00");
  const now = new Date();
  const diff = target.getTime() - now.getTime();
  if (diff <= 0) return { days: 0, hours: 0, minutes: 0, seconds: 0, isPast: true };
  return {
    days: Math.floor(diff / 86400000),
    hours: Math.floor((diff % 86400000) / 3600000),
    minutes: Math.floor((diff % 3600000) / 60000),
    seconds: Math.floor((diff % 60000) / 1000),
    isPast: false,
  };
}

export function getNextNationalDayYear(): number {
  const now = new Date();
  for (const year of NATIONAL_DAY_YEARS) {
    const data = NATIONAL_DAY_DATA[year];
    if (data && new Date(data.date + "T00:00:00") > now) return year;
  }
  return NATIONAL_DAY_YEARS[NATIONAL_DAY_YEARS.length - 1];
}
