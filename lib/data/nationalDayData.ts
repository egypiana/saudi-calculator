export interface NationalDayYearData {
  year: number;
  nationalDayNumber: number;
  date: string;
  dayOfWeek: string;
  isWeekend: boolean;
  season: "خريف";
  status: "past" | "current" | "upcoming" | "future";
  theme: string;
  content: {
    metaTitle: string;
    metaDescription: string;
    keywords: string[];
    heroSubtitle: string;
    uniqueFact: string;
    mainContent: string;
    preparations: string[];
    faqExtra: { q: string; a: string }[];
  };
}

function getStatus(date: string): NationalDayYearData["status"] {
  const now = new Date();
  const nd = new Date(date + "T00:00:00");
  const ndEnd = new Date(nd);
  ndEnd.setDate(ndEnd.getDate() + 1);
  if (now > ndEnd) return "past";
  if (now >= nd && now <= ndEnd) return "current";
  const diff = nd.getTime() - now.getTime();
  if (diff < 365 * 24 * 60 * 60 * 1000) return "upcoming";
  return "future";
}

export function formatDateAr(dateStr: string): string {
  const months: Record<string, string> = {
    "01": "يناير", "02": "فبراير", "03": "مارس", "04": "أبريل",
    "05": "مايو", "06": "يونيو", "07": "يوليو", "08": "أغسطس",
    "09": "سبتمبر", "10": "أكتوبر", "11": "نوفمبر", "12": "ديسمبر",
  };
  const [y, m, d] = dateStr.split("-");
  return `${parseInt(d)} ${months[m]} ${y}`;
}

function getDayOfWeek(dateStr: string): string {
  const days = ["الأحد", "الاثنين", "الثلاثاء", "الأربعاء", "الخميس", "الجمعة", "السبت"];
  return days[new Date(dateStr + "T00:00:00").getDay()];
}

function makeNationalDay(
  year: number,
  theme: string,
  heroSubtitle: string,
  uniqueFact: string,
  mainContent: string,
  preparations: string[],
  faqExtra: { q: string; a: string }[]
): NationalDayYearData {
  const nationalDayNumber = year - 1930;
  const date = `${year}-09-23`;
  const dayOfWeek = getDayOfWeek(date);
  const isWeekend = dayOfWeek === "الجمعة" || dayOfWeek === "السبت";
  return {
    year,
    nationalDayNumber,
    date,
    dayOfWeek,
    isWeekend,
    season: "خريف",
    status: getStatus(date),
    theme,
    content: {
      metaTitle: `اليوم الوطني السعودي ${nationalDayNumber} | ${year} — كم باقي على اليوم الوطني؟`,
      metaDescription: `العد التنازلي لليوم الوطني السعودي ${nationalDayNumber} الموافق ${formatDateAr(date)}. تعرف على شعار وفعاليات اليوم الوطني ${year} ومعلومات كاملة عن الاحتفالات.`,
      keywords: [
        `اليوم الوطني السعودي ${nationalDayNumber}`,
        `اليوم الوطني ${year}`,
        `كم باقي على اليوم الوطني ${year}`,
        `شعار اليوم الوطني ${nationalDayNumber}`,
      ],
      heroSubtitle,
      uniqueFact,
      mainContent,
      preparations,
      faqExtra,
    },
  };
}

